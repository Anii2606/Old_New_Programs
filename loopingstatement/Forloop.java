package com.loopingstatement;

import java.util.Scanner;

public class Forloop {

	public static void main(String[] args) {
int num;
		Scanner m=new Scanner(System.in);
	System.out.println("Enter number");
	num=m.nextInt();
for(int i=1;i>=10;i--) 
{
	System.out.println(num*i);
}
	}

}
